document.addEventListener("DOMContentLoaded", () => {
  const title = document.getElementById("hero-title");
  const text = document.getElementById("hero-text");

  const messages = [
      {
          title: "Project Showcase",
          text: "Discover amazing projects created by Open Minds Club members, from interactive games to beautiful profile cards."
      },
      {
          title: "About Open Minds Club",
          text: "We are a vibrant community of tech enthusiasts, focusing on creativity, learning, and collaboration through hands-on projects."
      }
  ];

  let currentIndex = 0;

  function changeText() {
      currentIndex = (currentIndex + 1) % messages.length;

      title.classList.remove("fade");
      text.classList.remove("fade");

      setTimeout(() => {
          title.innerText = messages[currentIndex].title;
          text.innerText = messages[currentIndex].text;

          title.classList.add("fade");
          text.classList.add("fade");
      }, 500); 
  }

  
  setInterval(changeText, 3000);
});
